package main

import "fmt"

func main(){
       var name string // by default it will be an empty string ""
	   fmt.Println(name)
	   
	   name="GOLANG"
	   fmt.Println(name)

	   var a int // By default it will print zero
	   fmt.Println(a)
	   a=777
	   fmt.Println(a)

	   var b bool // By default it will print false
	   fmt.Println(b)

}

